# Задача 12: Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница.
# Петя помогает Кате по математике. Он задумывает два натуральных числа X и Y (X,Y≤1000),
# а Катя должна их отгадать. Для этого Петя делает две подсказки.
# Он называет сумму этих чисел S и их произведение P.
# Помогите Кате отгадать задуманные Петей числа.


# Функция для поиска двух чисел X и Y по их сумме и произведению
def find_numbers(S, P):
    for X in range(1, S + 1):
        Y = S - X
        if X * Y == P:
            return X, Y
    return None, None

# Ввод суммы и произведения от Пети
S = int(input("Введите сумму чисел (S): "))
P = int(input("Введите произведение чисел (P): "))

# Поиск чисел X и Y
X, Y = find_numbers(S, P)

if X is not None and Y is not None:
    print(f"Задуманные числа: X = {X}, Y = {Y}")
else:
    print("Невозможно найти числа X и Y для заданных S и P.")
